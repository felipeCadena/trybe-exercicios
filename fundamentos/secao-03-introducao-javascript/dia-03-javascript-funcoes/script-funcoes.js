
let saldo = 2000;

function soma(valor) {
  return saldo + valor;
}

function subtrair(valor) {
  return saldo - valor;
  
}

function multi(valor) {
  return saldo * valor;
  
}

function div(valor) {
  return saldo / valor;
  
}

console.log(soma(1000));
console.log(subtrair(1000));
console.log(multi(1000));
console.log(div(1000));
